def generateCSV(file, _data):
    if GENERATE_CSV:
        # map circuit names
        circuit_name_mapping = {
            'qml_circuit_qiskit_01': 'q_circuit_01',
            'qml_circuit_qiskit_02': 'q_circuit_02',
            'qml_circuit_qiskit_03': 'q_circuit_03',
            'qml_circuit_qiskit_04': 'q_circuit_04',
            'qml_circuit_qiskit_05': 'q_circuit_05'
        }
        # create csv
        with open(file, 'w', encoding='UTF8') as f:
            writer = csv.writer(f)
            # write the header
            writer.writerow(csv_header)

            # write the data
            for dataset_key, circuit_dict in _data.items():
                for circuit_key, optimizer_array in circuit_dict.items():
                    for optimizer_dict in optimizer_array:
                        for (optimizer_key, optimizer_data) in optimizer_dict.items():
                            (dataset_id, accuracy_q_simulator, accuracy_q_computer) = optimizer_data
                            # Quantum Simulator
                            for score in accuracy_q_simulator:
                                writer.writerow(['quantum simulator', dataset_key, dataset_id, circuit_name_mapping[circuit_key], optimizer_key, score])
                            # Quantum Computer
                            for score in accuracy_q_computer:
                                writer.writerow(['quantum computer', dataset_key, dataset_id, circuit_name_mapping[circuit_key], optimizer_key, score])
    print(f"CSV data has been written to {file}")


